# function below is kept for backward compatibility but is no longer needed.
_WOW_TILE_SIZE = 533.33333333
_WOW_CHUNK_SIZE = _WOW_TILE_SIZE / 16.0  # ~33.333 yards per chunk
_WOW_MAP_SIZE_MAX = 17066.66657

# Elwynn Forest tile coordinates
