"""
Elwynn Forest Modification Test Script

Uses the full procedural pipeline to generate a modified ADT for Elwynn
Forest tile (3, 4) and packs it into patch-4.MPQ for the WoW 3.3.5a client.

Pipeline:
    plan_zone() -> sculpt_for_adt_composer() -> create_adt() -> MPQ pack

All terrain is seeded procedural generation via SimplexNoise in the
terrain_sculptor. The seed is configurable for reproducible results.

Usage:
    python tools/test_elwynn_modification.py [--seed N] [--archetype NAME]
    python tools/test_elwynn_modification.py --seed 42 --archetype forested_highlands
    python tools/test_elwynn_modification.py --archetype volcanic_island --no-copy
    python tools/test_elwynn_modification.py --list-archetypes

After running:
    1. The script copies patch-4.MPQ to the WoW Data directory
    2. Restart the WoW client
    3. Log in and visit Goldshire / Northshire in Elwynn Forest
    4. You should see procedurally generated terrain matching the archetype

To revert:
    Delete G:\\WoW AzerothCore\\Data\\patch-4.MPQ
"""

