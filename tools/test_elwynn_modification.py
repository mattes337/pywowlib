def fix_adt_mcnk_positions(adt_bytes, tile_x, tile_y):
    """Patch MCNK chunk positions in an ADT to use real WoW coordinates.

    DEPRECATED: adt_composer.py TILE_SIZE is now correct (533.333).
    This function is no longer needed and should not be called on ADTs
    generated by the current adt_composer. Kept for backward compatibility.

    MCNK header layout (128 bytes, starting after 8-byte chunk header):
        offset  0: flags (4B)
        offset  4: index_x/chunk_col (4B)
        offset  8: index_y/chunk_row (4B)
        ...
        offset 104: position x,y,z (3 floats, 12B)

    Args:
        adt_bytes: Raw ADT file bytes from create_adt().
        tile_x: Real WoW tile X coordinate (0-63).
        tile_y: Real WoW tile Y coordinate (0-63).

    Returns:
        bytes: Patched ADT bytes with correct MCNK positions.
    """
    data = bytearray(adt_bytes)
    mcnk_magic = b'KNCM'  # Reversed — WoW stores chunk magic in little-endian
    patched = 0

    i = 0
    while i < len(data) - 4:
        if data[i:i + 4] == mcnk_magic:
            # Found MCNK chunk at offset i
            # Chunk header: 4 bytes magic + 4 bytes size = 8 bytes
            # MCNK header starts at i+8
            hdr_start = i + 8

            # Read chunk_col (index_x) and chunk_row (index_y)
            chunk_col = struct.unpack_from('<I', data, hdr_start + 4)[0]
            chunk_row = struct.unpack_from('<I', data, hdr_start + 8)[0]

            # Calculate correct WoW position
            pos_x = _WOW_MAP_SIZE_MAX - (tile_y * _WOW_TILE_SIZE) - (chunk_row * _WOW_CHUNK_SIZE)
            pos_y = _WOW_MAP_SIZE_MAX - (tile_x * _WOW_TILE_SIZE) - (chunk_col * _WOW_CHUNK_SIZE)
            pos_z = 0.0

            # Patch position at MCNK header offset 104
            struct.pack_into('<fff', data, hdr_start + 104, pos_x, pos_y, pos_z)
            patched += 1

            # Skip past this MCNK (read its size to jump ahead)
            chunk_size = struct.unpack_from('<I', data, i + 4)[0]
            i += 8 + chunk_size
        else:
            i += 1

    if patched != 256:
        print("  WARNING: Patched {} MCNK chunks (expected 256)".format(patched))
    else:
        print("  Patched {} MCNK positions to tile ({}, {})".format(patched, tile_x, tile_y))

    return bytes(data)


